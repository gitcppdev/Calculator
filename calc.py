def sum(a, b):
    return a + b

def sub(a, b):
    sub_result = a - b
    print(f"Sub: {sub_result}")
    return sub_result

def mul(a, b):
    mul_result = a * b
    print(f"Mul: {mul_result}")
    return mul_result

def div(a, b):
    div_result = a / b
    print(f"Div: {div_result}")
    return div_result